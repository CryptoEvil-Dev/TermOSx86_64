#pragma once
#include <stdint.h>

namespace Storage {

// =========================================================================
// Тип подключения диска на ATA-шине (Master / Slave)
// =========================================================================
enum DriveType {
    ATA_MASTER = 0, // Master-устройство (обычно порт 0x1F0)
    ATA_SLAVE = 1,  // Slave-устройство
};

// =========================================================================
// Структура, описывающая один физический диск (ATA-устройство)
// =========================================================================
struct Drive {
    uint16_t base_port;     // Базовый порт ввода-вывода (0x1F0 для Primary, 0x170 для Secondary)
    DriveType type;         // Master или Slave
    bool exists;            // Флаг успешного обнаружения устройства
    char model[41];         // Модель диска (строка из 40 символов + завершающий 0)
    uint64_t size_sectors;  // Размер диска в секторах (обычно по 512 байт)
};

// =========================================================================
// Класс-менеджер дисковых операций через ATA
// Позволяет инициализировать диски, читать и записывать сектора.
// =========================================================================
class DiskManager {
public:
    // ---------------------------------------------------------------------
    // Инициализация подсистемы ATA: поиск подключённых дисков,
    // заполнение массива drives, проверка LBA28/LBA48 и т.д.
    // ---------------------------------------------------------------------
    void init();

    // ---------------------------------------------------------------------
    // Зарегистрировать диск вручную (используется, если автоопределение
    // не подходит или для добавления виртуальных дисков).
    // port - базовый порт (0x1F0, 0x170...), type - ATA_MASTER/ATA_SLAVE
    // ---------------------------------------------------------------------
    void register_driver(uint16_t port, DriveType type);

    // Чтение/Запись секторов
    bool read_sector(uint8_t drive_idx, uint32_t lba, uint8_t* buffer);
    bool write_sector(uint8_t drive_idx, uint32_t lba, uint8_t* buffer);

private:
    Drive drives[4]; // Поддержим до 4-х дисков
    uint8_t drive_count = 0;
};

// =========================================================================
// Вспомогательная функция: ожидание готовности ATA-контроллера
// (проверка флагов BSY и DRQ, обычно по порту status)
// Объявлена статической, т.е. видима только в пределах файла ata.cpp.
// =========================================================================
static void ata_wait_ready(uint16_t port);

// -------------------------------------------------------------------------
// Глобальный экземпляр менеджера дисков; используется драйвером FAT32
// для доступа к секторам. Singleton
// -------------------------------------------------------------------------
extern DiskManager disk_mgr;
}